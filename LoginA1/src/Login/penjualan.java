    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            if(tabel.getRowCount()==0){
                JOptionPane.showMessageDialog(null, "Silahkan Masukkan Data! ");}
            else if(tabel.getSelectedRow()< 0){
                JOptionPane.showMessageDialog(null, "Seleksi Terlebih Dahulu!");}
            else{
                if(tabel.getSelectedRow() >= 0) {

                    int input = JOptionPane.showConfirmDialog(null, "Yakin hapus data ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
                    if (input == 0){
                        String id_penjualan = tabel.getModel().getValueAt(tabel.getSelectedRow(), 0).toString();
                        String sql     = "delete from penjualan where id_penjualan=?";
                        ps = conn.prepareStatement(sql);
                        ps.setString(1, id_penjualan);
                        ps.executeUpdate();

                        JOptionPane.showMessageDialog(rootPane, "Data berhasil dihapus");
                        sinkron();
                    }
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

