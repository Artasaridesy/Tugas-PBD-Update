    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tampilan ="yyyy=MM-dd";
        SimpleDateFormat fm = new SimpleDateFormat(tampilan);
        String tgl = String.valueOf(fm.format(jCalendar1.getDate()));

        String sql = "insert into penjualan values (null, ?,?,?,?)";
        try {
            ps = conn.prepareStatement(sql);
            ps.setString(1,id_barang.getText());
            ps.setString(2,nama_barang.getText());
            ps.setString(3,tgl);
            ps.setString(4,harga.getText());
            ps.executeUpdate();
            
            if ("".equals(id_barang.getText())){
                JOptionPane.showMessageDialog(null, "Masukan id barang terlebih dahulu !","Message", JOptionPane.ERROR_MESSAGE);
                return;}
            else if ("".equals(nama_barang.getText())){
                JOptionPane.showMessageDialog(null, "Masukan Nama barang terlebih dahulu !","Message", JOptionPane.ERROR_MESSAGE);
                return;}
             else if ("".equals(harga.getText())){
                JOptionPane.showMessageDialog(null, "Masukan harga terlebih dahulu !","Message", JOptionPane.ERROR_MESSAGE);
                return;}
            else{
                JOptionPane.showMessageDialog(rootPane, "Data berhasil ditambahkan!");
                sinkron();
            } 
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

